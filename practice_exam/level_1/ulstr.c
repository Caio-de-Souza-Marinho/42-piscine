#include <unistd.h>

int	main(int argc, char **argv)
{
	int i;

	i = 0;
	if (argc != 1)
		write(1, "\n", 1);
	else
	{
		while (*argv[1] != '\0')
		{
			if (argv[1][i] >= 'A' && argv[1][i] <= 'Z')
			{
				argv[1][i] -= 32;
				i++;
			}
			else if (argv[1][i] >= 'a' && argv[1][i] <= 'z')
			{
				argv[1][i] += 32;
				i++;
			}
			else
				i++;
		}
		write(1, "\n", 1);
	}
	return (0);
}
